class SCR_Model_Unique_ID_List(PTCC_Model_ID_List):

    def __init__(self):

        PTCC_Model_ID_List.__init__(self)

    def add(self,obj):

        if obj.id not in [_local_obj.id for _local_obj in self.objects]:

            self.objects.append(obj) 


