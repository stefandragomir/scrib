class SCR_Config(object):


    def __init__(self):

        self.theme  = "light"
        self.themes = {
                        "light"  : {
                                    "background"        : "#FFFFFF",
                                    "foreground"        : "#000000",
                                   },
                                  
                        "dark" : {
                                    "background"        : "#272932",
                                    "foreground"        : "#FFFFFF",
                                 },
                      }

    def get_theme_background(self):

        return self.themes[self.theme]["background"]

    def get_theme_foreground(self):

        return self.themes[self.theme]["foreground"]

